# Write a Python program to demonstrate string slicing.
str = "hello world"
print(str[3:6])
print(str[:6])
print(str[3:])
print(str[::2])

# Write a Python program that manipulates and prints strings using various string methods.
str = "hello world"
print(str.strip())
print(str.upper())
print(str.lower())
print(str.capitalize())
print(str.title())
print(str.replace("l", "x"))
print(str.find("l"))
print(str.startswith("h"))
print(str.endswith("d"))
print(str.split())
print(str.isdigit())
print(str.isalpha())
print(str.isalnum())
print(str.zfill(15))
